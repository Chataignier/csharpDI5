namespace Mercure
	{
	//*********************************************************************************************
	// Classe Program
	//*********************************************************************************************
	/// <summary>
	/// Objet encapsulant le point d'entrée de l'application.
	/// </summary>
	//---------------------------------------------------------------------------------------------
	class Program
		{
		//*****************************************************************************************
		// Prototype   : static void Main (string[] Args)
		// Description : Point d'entrée de l'application
		//*****************************************************************************************
		/// <summary>
		/// Point d'entrée de l'application.
		/// </summary>
		/// <param name="Args">Paramètres de l'application.</param>
		//-----------------------------------------------------------------------------------------
		static void Main ( string[] Args )
			{
			//-------------------------------------------------------------------------------------
			// Connexion au Service-Web
			//-------------------------------------------------------------------------------------
			MercureService Service = new MercureService ();

			Service.Url = "http://localhost:8001/Sources/Mercure.asmx";
			//-------------------------------------------------------------------------------------

			//-------------------------------------------------------------------------------------
			// Récupération des articles & Affichage
			//-------------------------------------------------------------------------------------
			List<string[]> Articles = Service.GetArticles ();
			
			foreach ( string[] Datas in Articles )
				{
				foreach ( string Data in Datas ) { Console.WriteLine ( Data ); }
				}
			//-------------------------------------------------------------------------------------
			}
		//*****************************************************************************************
		}
